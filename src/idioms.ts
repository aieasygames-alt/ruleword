export function getRandomIdiom(): string {
  return IDIOMS[Math.floor(Math.random() * IDIOMS.length)]
}

// 验证成语是否在词库中
export function isValidIdiom(idiom: string): boolean {
  return IDIOMS.includes(idiom)
}

// 获取每日成语
export function getDailyIdiom(): string {
  const startDate = new Date('2024-01-01').getTime()
  const now = new Date().setHours(0, 0, 0, 0)
  const dayIndex = Math.floor((now - startDate) / 86400000)
  return IDIOMS[dayIndex % IDIOMS.length]
}
